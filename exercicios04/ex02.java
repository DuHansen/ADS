package app;

public class App {
    public static void main(String[] args) {
        int i;
        System.out.println("Numeros em ordem crescente: ");
        for(i = 1; i <11; i++){
            System.out.println("São:"+ i);
        }
        System.out.println("/////////////////");
        System.out.println("Numeros em ordem decrescente: ");
        int x = 10;
        while(x >= 1){
            System.out.println("São: " + x);
            x -- ;
        }
        

    }

}
